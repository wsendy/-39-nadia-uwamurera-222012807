public class forloop1//this is class name which is called forloop1.
{
    public static void main(String a[])//this is  main function in java programming which help ststement to be executed.
    {
        int i,sum=0;//this is variable declaration
        for(i=1;i<=10;i++)// this is loop which repeat from one to 10
        sum=sum+i;//this is for calculation of sum of those numbers from 1 to 10
        System.out.println("the sum is:" +sum);//this statement to display the sum of those numbers.
    }
}